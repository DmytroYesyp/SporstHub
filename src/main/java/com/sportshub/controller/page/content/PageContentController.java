package com.sportshub.controller.page.content;


import com.sportshub.dto.page.content.PageContentDto;
import com.sportshub.service.page.content.PageContentService;
import lombok.AllArgsConstructor;
import org.springframework.http.HttpStatus;
import org.springframework.web.bind.annotation.*;

@RestController
@AllArgsConstructor
@RequestMapping("page-content")
public class PageContentController {

    private final PageContentService pageContentService;

    @GetMapping("{id}")
    public PageContentDto find(@PathVariable Long id) { return pageContentService.find(id); }

    @PutMapping("{id}")
    @ResponseStatus(HttpStatus.NO_CONTENT)
    public void update(@PathVariable Long id, @RequestBody PageContentDto pageContentDto) { pageContentService.update(id, pageContentDto); }

}
